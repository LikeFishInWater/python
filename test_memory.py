for j in range(368):
    ops[j] = (math.e**(x[j] * W[j])-math.e**(-x[j] * W[j]))/(math.e**(x[j] * W[j])+math.e**(-x[j] * W[j]))
model_output = 0
for j in range(368):
    model_output += ops[j]

init = tf.global_variables_initializer()
learning_rate = 0.01
my_opt = tf.train.GradientDescentOptimizer(learning_rate)
loss = tf.reduce_mean(tf.square(model_output - y))
train = my_opt.minimize(loss)

sess = tf.Session()
sess.run(init)

print('initial: W=' + str(sess.run(W)))
for i in range(1000):
    sess.run(train, feed_dict={x: x_vals[i], y: y_vals[i]})
    temp_loss = sess.run(loss, feed_dict={x: x_vals[i], y: y_vals[i]})
    if i % 10 == 0:
        print('temp_loss=' + str(temp_loss))
